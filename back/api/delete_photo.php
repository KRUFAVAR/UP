require 'db.php'; // Подключение к базе данных

// Начало сессии, если необходимо
session_start();

// Предполагается, что данные о пользователе уже загружены в переменную $user

if ($_SERVER['REQUEST_METHOD'] === 'POST') {
    // Удаление фото из папки
    $photo = "uploads2/" . $user->photo; // Имя файла фото
    if (file_exists($photo)) {
        unlink($photo);
        // Обновление информации о пользователе в базе данных
        $user->deletePhoto(); // Пример удаления фото из базы данных
        echo "Фото успешно удалено.";
    } else {
        echo "Фото не найдено.";
